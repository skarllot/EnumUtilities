using Raiqub.Generators.EnumUtilities.Common;
using Raiqub.Generators.EnumUtilities.Models;
using Raiqub.Generators.InterpolationCodeWriter;

namespace Raiqub.Generators.EnumUtilities.CodeWriters;

public sealed class EnumJsonConverterWriter : ICodeWriter<EnumToGenerate>
{
    public bool CanGenerateFor(EnumToGenerate model) =>
        (model.SelectedGenerators & SelectedGenerators.JsonConverter) != SelectedGenerators.None
        && model.JsonConverterGeneratorOptions is not null;

    public string GetFileName(EnumToGenerate model) => CodeWriterHelper.GetFileName(model, "JsonConverter");

    public void Write(SourceTextWriter writer, EnumToGenerate model)
    {
        writer.WriteLine(
            """
            // <auto-generated />
            #nullable enable

            using System;
            using System.Buffers;
            using System.Text.Json;
            using System.Text.Json.Serialization;
            using Raiqub.Generators.EnumUtilities.Formatters;
            using Raiqub.Generators.EnumUtilities.Parsers;

            #pragma warning disable CS1591 // publicly visible type or member must be documented
            """
        );
        writer.WriteLine();

        if (!string.IsNullOrEmpty(model.Namespace))
        {
            writer.WriteLine($"namespace {model.Namespace};");
            writer.WriteLine();
        }

        CodeWriterHelper.WriteGeneratedCodeAttributes(writer);

        writer.WriteLine(
            $$"""
              {{(
                  model.IsPublic ? "public" : "internal"
              )}} sealed partial class {{model.Name}}JsonConverter : JsonConverter<{{model.Name}}>
              {
                  private const int MaxCharStack = 256;
              """
        );
        writer.PushIndent();

        writer.WriteLine();
        WriteRead(writer, model);

        writer.WriteLine();
        WriteWrite(writer, model);

        writer.WriteLine();
        WriteReadFromString(writer, model);

        if (model.JsonConverterGeneratorOptions!.AllowIntegerValues)
        {
            writer.WriteLine();
            WriteReadFromNumber(writer, model);
        }

        writer.PopIndent();
        writer.WriteLine('}');
    }

    private static void WriteRead(SourceTextWriter writer, EnumToGenerate model)
    {
        writer.WriteLine(
            $$"""
            public override {{model.RefName}} Read(ref Utf8JsonReader reader, Type typeToConvert, JsonSerializerOptions options)
            {
                if (reader.TokenType == JsonTokenType.String)
                    return ReadFromString(ref reader);
            """
        );
        writer.PushIndent();

        if (model.JsonConverterGeneratorOptions!.AllowIntegerValues)
        {
            writer.WriteLine(
                $"""
                if (reader.TokenType == JsonTokenType.Number)
                    return ({model.RefName})ReadFromNumber(ref reader);
                """
            );
        }

        writer.WriteLine();
        writer.WriteLine($"{AppendFallbackValue(model, addReturn: true, castToEnum: true)};");

        writer.PopIndent();
        writer.WriteLine('}');
    }

    private static void WriteWrite(SourceTextWriter writer, EnumToGenerate model)
    {
        writer.WriteLine(
            $$"""
            public override void Write(Utf8JsonWriter writer, {{model.RefName}} value, JsonSerializerOptions options)
            {
                string? jsonString = value.ToJsonString();
                if (jsonString is not null)
                {
                    writer.WriteStringValue(jsonString);
                }
            """
        );
        writer.PushIndent();

        if (model.JsonConverterGeneratorOptions!.AllowIntegerValues)
        {
            writer.WriteLine(
                $$"""
                else
                {
                    writer.WriteNumberValue(({{model.UnderlyingType}})value);
                }
                """
            );
        }
        else if (model.JsonConverterGeneratorOptions.DeserializationFailureFallbackValue is not null)
        {
            writer.WriteLine(
                $$"""
                else
                {
                    jsonString = (({{model.RefName}}){{model.JsonConverterGeneratorOptions.DeserializationFailureFallbackValue}}).ToJsonString();
                    if (jsonString is not null)
                        writer.WriteStringValue(jsonString);
                    else
                        throw new JsonException();
                }
                """
            );
        }
        else
        {
            writer.WriteLine(
                """
                else
                {
                    throw new JsonException();
                }
                """
            );
        }

        writer.PopIndent();
        writer.WriteLine('}');
    }

    private static void WriteReadFromString(SourceTextWriter writer, EnumToGenerate model)
    {
        var ignoreCase = model.JsonConverterGeneratorOptions!.IgnoreCase ? "true" : "false";
        writer.WriteLine(
            $$"""
            #if NET7_0_OR_GREATER

            private {{model.RefName}} ReadFromString(ref Utf8JsonReader reader)
            {
                int length = reader.HasValueSequence ? checked((int)reader.ValueSequence.Length) : reader.ValueSpan.Length;

                char[]? rented = null;
                Span<char> name = length <= MaxCharStack ? stackalloc char[MaxCharStack] : (rented = ArrayPool<char>.Shared.Rent(length));
                try
                {
                    int charsWritten = reader.CopyString(name);
                    name = name.Slice(0, charsWritten);

                    bool isParsed = {{model.Name}}Factory.TryParseJsonString(name, ignoreCase: {{ignoreCase}}, out {{model.RefName}} result);
                    if (!isParsed)
                    {
                        {{AppendFallbackValue(model, addReturn: true, castToEnum: true)}};
                    }

                    return result;
                }
                finally
                {
                    if (rented != null)
                    {
                        ArrayPool<char>.Shared.Return(rented);
                    }
                }
            }

            #else

            private {{model.RefName}} ReadFromString(ref Utf8JsonReader reader)
            {
                var name = reader.GetString();
                bool isParsed = {{model.Name}}Factory.TryParseJsonString(name, ignoreCase: {{ignoreCase}}, out {{model.RefName}} result);
                if (!isParsed)
                {
                    {{AppendFallbackValue(model, addReturn: true, castToEnum: true)}};
                }

                return result;
            }

            #endif
            """
        );
    }

    private static void WriteReadFromNumber(SourceTextWriter writer, EnumToGenerate model)
    {
        var underlyingTypeName = CSharpExtensions.GetTypeNameFromCSharpKeyword(model.UnderlyingType);
        writer.WriteLine(
            $$"""
            private {{model.UnderlyingType}} ReadFromNumber(ref Utf8JsonReader reader)
            {
                return reader.TryGet{{underlyingTypeName}}(out {{model.UnderlyingType}} value)
                    ? value
                    : {{AppendFallbackValue(model)}};
            }
            """
        );
    }

    private static TextSegment AppendFallbackValue(
        EnumToGenerate model,
        bool addReturn = false,
        bool castToEnum = false
    )
    {
        var fallbackValue = model.JsonConverterGeneratorOptions!.DeserializationFailureFallbackValue;
        if (fallbackValue is null)
            return "throw new JsonException()";

        return (addReturn, castToEnum, NeedNumberCasting(model)) switch
        {
            (false, false, false) => $"{fallbackValue}",
            (false, false, true) => $"({model.UnderlyingType}){fallbackValue}",
            (false, true, _) => $"({model.RefName}){fallbackValue}",
            (true, false, false) => $"return {fallbackValue}",
            (true, false, true) => $"return ({model.UnderlyingType}){fallbackValue}",
            (true, true, _) => $"return ({model.RefName}){fallbackValue}",
        };
    }

    private static bool NeedNumberCasting(EnumToGenerate model) =>
        model.UnderlyingType is "byte" or "short" or "sbyte" or "ushort";
}
