using System.Reflection;
using Raiqub.Generators.EnumUtilities.CodeWriters.Extensions;
using Raiqub.Generators.EnumUtilities.Common;
using Raiqub.Generators.EnumUtilities.Models;
using Raiqub.Generators.InterpolationCodeWriter;

namespace Raiqub.Generators.EnumUtilities.CodeWriters;

public class EnumExtensionsWriter : ICodeWriter<EnumToGenerate>
{
    private static readonly ICodeWriterModule<EnumToGenerate>[] s_modules =
    [
        new ExtensionsDefaultBlock(),
        new ExtensionsInterlockedBlock(),
        new ExtensionsEnumMemberBlock(),
        new ExtensionsDescriptionBlock(),
        new ExtensionsDisplayBlock(),
        new ExtensionsMatchBlock(),
        new ExtensionsJsonBlock(),
    ];

    private static AssemblyName CurrentAssemblyName { get; } = typeof(EnumExtensionsWriter).Assembly.GetName();

    public bool CanGenerateFor(EnumToGenerate model) =>
        (model.SelectedGenerators & (SelectedGenerators.MainGenerator | SelectedGenerators.JsonConverter))
        != SelectedGenerators.None;

    public string GetFileName(EnumToGenerate model) => CodeWriterHelper.GetFileName(model, "Extensions");

    public void Write(SourceTextWriter writer, EnumToGenerate model)
    {
        writer.WriteLine(
            """
            // <auto-generated />
            #nullable enable
            """
        );

        writer.WriteLine();
        writer.WriteNamespaceImports(
            s_modules,
            model,
            defaultImports: ["System", "System.Runtime.CompilerServices", "Raiqub.Generators.EnumUtilities.Formatters"]
        );
        writer.WriteLine();

        writer.WriteLine("#pragma warning disable CS1591 // publicly visible type or member must be documented");
        writer.WriteLine();

        if (!string.IsNullOrEmpty(model.Namespace))
        {
            writer.WriteLine($"namespace {model.Namespace};");
            writer.WriteLine();
        }

        writer.WriteLine(
            $$"""
            [global::System.Diagnostics.CodeAnalysis.ExcludeFromCodeCoverage]
            [global::System.CodeDom.Compiler.GeneratedCodeAttribute("{{CurrentAssemblyName.Name}}", "{{CurrentAssemblyName.Version}}")]
            {{(model.IsPublic ? "public" : "internal")}} static partial class {{model.Name}}Extensions
            {
            """
        );

        writer.PushIndent();

        if (model.IsFlags)
        {
            WriteFlagsFields(writer, model);
            writer.WriteLine();
        }

        writer.WriteAll(s_modules, model, static w => w.WriteLine());

        writer.PopIndent();
        writer.WriteLine('}');
    }

    private static void WriteFlagsFields(SourceTextWriter writer, EnumToGenerate model)
    {
        var validFlags = model.FlagsInfo!.ValidFlagsMask;
        var numSuffix = CSharpExtensions.GetNumericSuffixFromCSharpKeyword(model.UnderlyingType);
        writer.WriteLine($"private const {model.UnderlyingType} ValidFlagsMask = {validFlags}{numSuffix};");
    }
}
