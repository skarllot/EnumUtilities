using Raiqub.Generators.EnumUtilities.CodeWriters.Factory;
using Raiqub.Generators.EnumUtilities.Models;
using Raiqub.Generators.InterpolationCodeWriter;

namespace Raiqub.Generators.EnumUtilities.CodeWriters;

public class EnumFactoryWriter : ICodeWriter<EnumToGenerate>
{
    private static readonly ICodeWriterModule<EnumToGenerate>[] s_modules =
    [
        new FactoryDefaultBlock(),
        new FactoryEnumMemberBlock(),
        new FactoryDescriptionBlock(),
        new FactoryDisplayBlock(),
        new FactoryMiscellaneousBlock(),
        new FactoryJsonBlock(),
    ];

    public bool CanGenerateFor(EnumToGenerate model) =>
        (model.SelectedGenerators & (SelectedGenerators.MainGenerator | SelectedGenerators.JsonConverter))
        != SelectedGenerators.None;

    public string GetFileName(EnumToGenerate model) => CodeWriterHelper.GetFileName(model, "Factory");

    public void Write(SourceTextWriter writer, EnumToGenerate model)
    {
        writer.WriteLine(
            """
            // <auto-generated />
            #nullable enable

            using System;
            using System.Diagnostics.CodeAnalysis;
            using System.Globalization;
            using System.Runtime.CompilerServices;
            using Raiqub.Generators.EnumUtilities.Parsers;

            #pragma warning disable CS1591 // publicly visible type or member must be documented
            """
        );
        writer.WriteLine();

        if (!string.IsNullOrEmpty(model.Namespace))
        {
            writer.WriteLine($"namespace {model.Namespace};");
            writer.WriteLine();
        }

        CodeWriterHelper.WriteGeneratedCodeAttributes(writer);
        writer.WriteLine(
            $$"""
            {{(model.IsPublic ? "public" : "internal")}} static partial class {{model.Name}}Factory
            {
            """
        );

        writer.PushIndent();

        writer.WriteAll(s_modules, model, static w => w.WriteLine());

        writer.PopIndent();
        writer.WriteLine('}');
    }
}
