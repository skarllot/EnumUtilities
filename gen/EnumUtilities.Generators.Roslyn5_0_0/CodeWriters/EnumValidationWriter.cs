using Raiqub.Generators.EnumUtilities.Models;
using Raiqub.Generators.InterpolationCodeWriter;

namespace Raiqub.Generators.EnumUtilities.CodeWriters;

public sealed class EnumValidationWriter : ICodeWriter<EnumToGenerate>
{
    public bool CanGenerateFor(EnumToGenerate model) =>
        (model.SelectedGenerators & SelectedGenerators.MainGenerator) != SelectedGenerators.None;

    public string GetFileName(EnumToGenerate model) => CodeWriterHelper.GetFileName(model, "Validation");

    public void Write(SourceTextWriter writer, EnumToGenerate model)
    {
        writer.WriteLine(
            """
            // <auto-generated />
            #nullable enable

            using System;
            using System.Diagnostics.CodeAnalysis;

            #pragma warning disable CS1591 // publicly visible type or member must be documented
            """
        );
        writer.WriteLine();

        if (!string.IsNullOrEmpty(model.Namespace))
        {
            writer.WriteLine($"namespace {model.Namespace};");
            writer.WriteLine();
        }

        CodeWriterHelper.WriteGeneratedCodeAttributes(writer);

        writer.WriteLine(
            $$"""
            {{(model.IsPublic ? "public" : "internal")}} static partial class {{model.Name}}Validation
            {
            """
        );
        writer.PushIndent();

        WriteIsDefinedEnumValue(writer, model);

        writer.WriteLine();
        WriteIsDefinedStringWithComparison(writer, model);

        writer.WriteLine();
        WriteIsDefinedIgnoreCase(writer);

        writer.WriteLine();
        WriteIsDefinedString(writer, model);

        writer.PopIndent();
        writer.WriteLine('}');
    }

    private static void WriteIsDefinedEnumValue(SourceTextWriter writer, EnumToGenerate model)
    {
        writer.WriteLine(
            $$"""
            /// <summary>Returns a boolean telling whether the value of <see cref="{{model.Name}}"/> instance exists in the enumeration.</summary>
            /// <returns><c>true</c> if the value of <see cref="{{model.Name}}"/> instance exists in the enumeration; <c>false</c> otherwise.</returns>
            public static bool IsDefined({{model.RefName}} value)
            {
                return ({{model.UnderlyingType}})value switch
                {
            """
        );
        writer.PushIndent(levels: 2);

        foreach (var curr in model.UniqueValues)
        {
            writer.WriteLine($"{curr.MemberValue} => true,");
        }

        writer.WriteLine("_ => false");

        writer.PopIndent();
        writer.WriteLine("};");

        writer.PopIndent();
        writer.WriteLine('}');
    }

    private static void WriteIsDefinedStringWithComparison(SourceTextWriter writer, EnumToGenerate model)
    {
        writer.WriteLine(
            """
            public static bool IsDefined(
                [NotNullWhen(true)] string? name,
                StringComparison comparisonType)
            {
                return name switch
                {
            """
        );
        writer.PushIndent(levels: 2);

        foreach (var curr in model.Values)
        {
            writer.WriteLine($"{{ }} s when s.Equals(\"{curr.MemberName}\", comparisonType) => true,");
        }

        writer.WriteLine("_ => false");

        writer.PopIndent();
        writer.WriteLine("};");

        writer.PopIndent();
        writer.WriteLine('}');
    }

    private static void WriteIsDefinedString(SourceTextWriter writer, EnumToGenerate model)
    {
        writer.WriteLine(
            """
            public static bool IsDefined([NotNullWhen(true)] string? name)
            {
                return name switch
                {
            """
        );
        writer.PushIndent(levels: 2);

        foreach (var curr in model.Values)
        {
            writer.WriteLine($"\"{curr.MemberName}\" => true,");
        }

        writer.WriteLine("_ => false");

        writer.PopIndent();
        writer.WriteLine("};");

        writer.PopIndent();
        writer.WriteLine('}');
    }

    private static void WriteIsDefinedIgnoreCase(SourceTextWriter writer)
    {
        writer.WriteLine(
            """
            public static bool IsDefinedIgnoreCase([NotNullWhen(true)] string? name)
            {
                return IsDefined(name, StringComparison.OrdinalIgnoreCase);
            }
            """
        );
    }
}
